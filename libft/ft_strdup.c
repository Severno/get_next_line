#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s2;

	s2 = NULL;
	i = 0;
	while (s1[i])
		i++;
	s2 = (char *)malloc(i + 1);
	i = 0;
	if (!s2)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = s1[i];
	return (s2);
}
